#include <iostream>
using namespace std;

int main() 
{    

    int a;
    float b; 
    char c;
    double d;


    cout << " Int Size: " << sizeof(a) << endl;
    cout << " Float Size: " << sizeof(b) << endl;
    cout << " Double Size: " << sizeof(d) << endl;
    cout << " Character Size: " << sizeof(c) << endl;

    return 0;
}